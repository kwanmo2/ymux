export type Lang =
  | "en" | "ko" | "ja" | "zh" | "hi" | "es"
  | "fr" | "ar" | "pt" | "ru" | "tr" | "de" | "vi";

const STORAGE_KEY = "ymux-lang";
let current: Lang = "en";
const listeners = new Set<() => void>();

export const ALL_LANGS: { code: Lang; label: string }[] = [
  { code: "en", label: "English" },
  { code: "ko", label: "한국어" },
  { code: "ja", label: "日本語" },
  { code: "zh", label: "中文" },
  { code: "hi", label: "हिन्दी" },
  { code: "es", label: "Español" },
  { code: "fr", label: "Français" },
  { code: "ar", label: "العربية" },
  { code: "pt", label: "Português" },
  { code: "ru", label: "Русский" },
  { code: "tr", label: "Türkçe" },
  { code: "de", label: "Deutsch" },
  { code: "vi", label: "Tiếng Việt" },
];

const VALID: Set<string> = new Set(ALL_LANGS.map((l) => l.code));

export function initLang(): void {
  try {
    const stored = localStorage.getItem(STORAGE_KEY);
    if (stored && VALID.has(stored)) current = stored as Lang;
  } catch { /* localStorage unavailable */ }
}

export function getLang(): Lang { return current; }

export function setLang(lang: Lang): void {
  if (current === lang) return;
  current = lang;
  try { localStorage.setItem(STORAGE_KEY, lang); } catch { /* */ }
  for (const cb of listeners) cb();
}

export function onLangChange(cb: () => void): () => void {
  listeners.add(cb);
  return () => { listeners.delete(cb); };
}

export function t(key: string): string {
  const entry = strings[key];
  if (!entry) return key;
  return entry[current] ?? entry.en ?? key;
}

